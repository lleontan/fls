DOCKER_BUILD_NAME="$DOCKER_USERNAME/fls-client"
DOCKER_IMAGE_NAME="flsClient"
REMOVE_ALL_DOCKER_CONTAINERS="docker rm -vf \$(docker ps -a -q)"
docker pull $DOCKER_BUILD_NAME;

